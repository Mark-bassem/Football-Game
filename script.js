function getRandomQuestions(count) {
  const shuffled = allQuestions.sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
}

const questions = getRandomQuestions(10);

let currentQuestion = 0;
let score = 0;
let timeLeft = 15;
let timerInterval;

const questionEl = document.getElementById("question");
const answersEl = document.getElementById("answers");
const nextBtn = document.getElementById("next-btn");
const resultBox = document.getElementById("result-box");
const scoreEl = document.getElementById("score");

const timerEl = document.createElement("p");
timerEl.style.fontWeight = "bold";
timerEl.style.fontSize = "18px";
document.getElementById("question-box").prepend(timerEl);

function startTimer() {
  timeLeft = 15;
  timerEl.textContent = `الوقت المتبقي: ${timeLeft} ثانية`;

  timerInterval = setInterval(() => {
    timeLeft--;
    timerEl.textContent = `الوقت المتبقي: ${timeLeft} ثانية`;

    if (timeLeft === 0) {
      clearInterval(timerInterval);
      showCorrectAnswer(null); // يعتبره لم يجاوب
      nextBtn.style.display = "inline-block";
    }
  }, 1000);
}

function stopTimer() {
  clearInterval(timerInterval);
}

function showQuestion() {
  const q = questions[currentQuestion];
  questionEl.textContent = q.q; // ✅ كان q.question
  answersEl.innerHTML = "";
  nextBtn.style.display = "none";

  q.options.forEach((option, index) => { // ✅ كان q.answers
    const btn = document.createElement("button");
    btn.textContent = option;
    btn.onclick = () => {
      stopTimer();
      selectAnswer(option); // نمرر النص بدلًا من الفهرس
    };
    answersEl.appendChild(btn);
  });

  startTimer();
}

function selectAnswer(selectedText) {
  showCorrectAnswer(selectedText);
  nextBtn.style.display = "inline-block";
}

function showCorrectAnswer(selectedText) {
  const correctText = questions[currentQuestion].answer;
  const buttons = answersEl.querySelectorAll("button");

  buttons.forEach((btn) => {
    btn.disabled = true;
    if (btn.textContent === correctText) {
      btn.style.backgroundColor = "green";
    } else if (btn.textContent === selectedText) {
      btn.style.backgroundColor = "red";
    }
  });

  if (selectedText === correctText) {
    score++;
    correctSound.play();
  } else {
    wrongSound.play();
  }
}

nextBtn.onclick = () => {
  currentQuestion++;
  if (currentQuestion < questions.length) {
    stopTimer();
    showQuestion();
  } else {
    showResult();
  }
};

function showResult() {
  document.getElementById("question-box").classList.add("hidden");
  resultBox.classList.remove("hidden");
  scoreEl.textContent = `أجبت بشكل صحيح على ${score} من ${questions.length} أسئلة.`;
}

